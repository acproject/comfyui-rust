pub fn get_models_by_category(category: &ModelCategory) -> Vec<ModelKnowledge> {
    get_model_knowledge_base()
        .into_iter()
        .filter(|m| m.category == *category)
        .collect()
}

pub fn get_models_by_style(style: &str) -> Vec<ModelKnowledge> {
    let style_lower = style.to_lowercase();
    get_model_knowledge_base()
        .into_iter()
        .filter(|m| {
            m.style_tags.iter().any(|t| t.to_lowercase().contains(&style_lower))
                || m.name.to_lowercase().contains(&style_lower)
                || m.description.to_lowercase().contains(&style_lower)
        })
        .collect()
}

pub fn get_template_by_id(id: &str) -> Option<WorkflowTemplate> {
    get_workflow_templates().into_iter().find(|t| t.id == id)
}

pub fn get_templates_by_category(category: &str) -> Vec<WorkflowTemplate> {
    get_workflow_templates()
        .into_iter()
        .filter(|t| t.category == category)
        .collect()
}
