use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ModelKnowledge {
    pub name: String,
    pub category: ModelCategory,
    pub description: String,
    pub trigger_tokens: Vec<String>,
    pub recommended_settings: RecommendedSettings,
    pub base_model: BaseModel,
    pub style_tags: Vec<String>,
    pub negative_prompt_suggestion: String,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub enum ModelCategory {
    Anime,
    Realistic,
    Artistic,
    Photorealistic,
    Fantasy,
    Cartoon,
    PixelArt,
    InkArt,
    Cyberpunk,
    Landscape,
    Portrait,
    General,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub enum BaseModel {
    SD15,
    SD21,
    SDXL,
    SD3,
    Flux,
    Flux2,
    Other(String),
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecommendedSettings {
    pub steps: u32,
    pub cfg: f64,
    pub sampler: String,
    pub scheduler: String,
    pub resolution: String,
    pub denoise: f64,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WorkflowTemplate {
    pub id: String,
    pub name: String,
    pub description: String,
    pub category: String,
    pub nodes: Vec<TemplateNode>,
    pub connections: Vec<TemplateConnection>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TemplateNode {
    pub id: String,
    pub class_type: String,
    pub title: String,
    pub x: i32,
    pub y: i32,
    pub inputs: serde_json::Value,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TemplateConnection {
    pub source: String,
    pub source_handle: String,
    pub target: String,
    pub target_handle: String,
}

